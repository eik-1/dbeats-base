import {
  NewNFT as NewNFTEvent,
  OwnershipTransferred as OwnershipTransferredEvent,
  RoleAdminChanged as RoleAdminChangedEvent,
  RoleGranted as RoleGrantedEvent,
  RoleRevoked as RoleRevokedEvent
} from "../generated/DBeatsFactory/DBeatsFactory"
import {
  NewNFT,
  OwnershipTransferred,
  RoleAdminChanged,
  RoleGranted,
  RoleRevoked
} from "../generated/schema"

export function handleNewNFT(event: NewNFTEvent): void {
  let entity = new NewNFT(
    event.transaction.hash.concatI32(event.logIndex.toI32())
  )
  entity.nftAddress = event.params.nftAddress
  entity._artistAddress = event.params._artistAddress
  entity._newTokenURI = event.params._newTokenURI
  entity.name = event.params.name
  entity.symbol = event.params.symbol
  entity.mintPrice = event.params.mintPrice
  entity._genre = event.params._genre

  entity.blockNumber = event.block.number
  entity.blockTimestamp = event.block.timestamp
  entity.transactionHash = event.transaction.hash

  entity.save()
}

